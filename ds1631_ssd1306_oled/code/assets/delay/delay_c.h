#ifndef DELAY_C
#define DELAY_C
//------------------------------------------------------------------------------
void delay_ms(int n) 
{
  int i;
  for(; n > 0; n--)
    for(i = 0; i < 3195; i++);
}
//------------------------------------------------------------------------------
void delay_us(int time)
{
	for(int i = 0; i < time; i++)
		for(int j = 0; j < 7; j++);
}
//------------------------------------------------------------------------------
#endif
